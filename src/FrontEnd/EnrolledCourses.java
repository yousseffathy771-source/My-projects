 private void addCertificateButtons()
 {
     for(int i=0;i<jTable1.getRowCount();i++)
     {
         String statues = jTable1.getValueAt(i, 4).toString();
         
         if(statues.equals("Certificate Earned"))
         {
             String CourseID=jTable1.getValueAt(i, 0).toString();
             
             JPanel buttonPanel=new JPanel();
             
             JButton viewButton=new JButton("view");
             viewButton.addActionListener(e-> viewCertificate(CourseID));
             
             JButton downloadButton = new JButton("download");
             downloadButton.addActionListener(e-> downloadCertificate(CourseID));
             
             buttonPanel.add(viewButton);
             buttonPanel.add(downloadButton);
             
             jTable1.setValueAt(buttonPanel, i, 5);
       
         }
     }
     

     
 }

 private void viewCertificate(String Cid)
 {
    try{
        
        String studentID=this.parentDashboard.getStudentId();
        Certificate cert = certificateService.GenerateCertificate(studentID, Cid);
        
        if(cert!=null)
        {
            certificateService.StoreCertificate(cert);
            ObjectNode JsonCert=certificateService.ConvertCertificateToJSON(cert);
            
            String certText=String.format("CERTIFICATE OF COMPLETION\n\nstudent:%s\ncourse:%s\ncertificateId:%s",studentID,Cid,cert.getCertificateID());
            
            JOptionPane.showMessageDialog(this,certText,"Certificate",JOptionPane.INFORMATION_MESSAGE);
        }
        
        
        
    }
     catch(Exception e)
     { 
        JOptionPane.showMessageDialog(this,"Error:"+e.getMessage());
     }
      
 }
 
 
 private void downloadCertificate(String Cid)
 {
      try{
        
        String studentID=this.parentDashboard.getStudentId();
        Certificate cert = certificateService.GenerateCertificate(studentID, Cid);
        
        if(cert!=null)
        {
            certificateService.StoreCertificate(cert);
            ObjectNode JsonCert=certificateService.ConvertCertificateToJSON(cert);
            ObjectMapper mapper = new ObjectMapper();
            String filename = "Certificate_"+cert.getCertificateID()+".json";
            File file = new File(filename);
            mapper.writerWithDefaultPrettyPrinter().writeValue(file,JsonCert);
            
            JOptionPane.showMessageDialog(this,"Saved: "+filename);
            
        }
      }
      
     catch(Exception e)
     { 
        JOptionPane.showMessageDialog(this,"Error:"+e.getMessage());
     }
      
      
     
     
     
 }
    
