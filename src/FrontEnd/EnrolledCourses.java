 private void loadEnrolledCourses() {
    List<Course> enrolled = studentService.viewEnrolledCourses();

    String[] columnNames = {"Course ID", "Course Name", "Instructor", "Description", "Status", "Certificate"};
    Object[][] data = new Object[enrolled.size()][6];

    for (int i = 0; i < enrolled.size(); i++) {
        Course c = enrolled.get(i);
        data[i][0] = c.getCourseId();
        data[i][1] = c.getTitle();
        data[i][2] = c.getInstructorId();
        data[i][3] = c.getDescription();
        
        String studentID = parentDashboard.getStudentId();
        
        if (!certificateService.isCourseCompleted(studentID, c.getCourseId())) {
            data[i][4] = "In progress";
            data[i][5] = "N/A";
        } else {
            data[i][4] = "Certificate Earned - Click to View";
            data[i][5] = c.getCourseId();
        }
    }

    DefaultTableModel model = new DefaultTableModel(data, columnNames) {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false;
        }
    };
    jTable1.setModel(model);
}

// Modify your setupListeners method to handle certificate clicks
private void setupListeners() {
    jTable1.addMouseListener(new java.awt.event.MouseAdapter() {
        public void mouseClicked(java.awt.event.MouseEvent evt) {
            int row = jTable1.getSelectedRow();
            int column = jTable1.getSelectedColumn();
            
            if (row >= 0) {
                if (column == 4) { // Status column
                    String status = jTable1.getValueAt(row, 4).toString();
                    if (status.contains("Certificate Earned")) {
                        String courseId = jTable1.getValueAt(row, 0).toString();
                        showCertificateOptions(courseId);
                    }
                } else if (column == 0) { // Course ID column - double click for lessons
                    if (evt.getClickCount() == 2) {
                        String courseId = jTable1.getValueAt(row, 0).toString();
                        Lessons lesson = parentDashboard.getLessonPanel(courseId);
                        lesson.loadlessons(courseId);
                        
                        CardLayout cl = (CardLayout) parentDashboard.getContentPanel().getLayout();
                        cl.show(parentDashboard.getContentPanel(), "lesson");
                    }
                }
            }
        }
    });
}

private void showCertificateOptions(String courseId) {
    String[] options = {"View Certificate", "Download Certificate", "Cancel"};
    int choice = JOptionPane.showOptionDialog(this,
            "Choose an action for the certificate:",
            "Certificate Options",
            JOptionPane.DEFAULT_OPTION,
            JOptionPane.INFORMATION_MESSAGE,
            null,
            options,
            options[0]);
    
    switch (choice) {
        case 0:
            viewCertificate(courseId);
            break;
        case 1:
            downloadCertificate(courseId);
            break;
        default:
            break;
    }
}
